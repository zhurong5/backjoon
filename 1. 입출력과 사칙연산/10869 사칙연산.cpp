#include <iostream>
using namespace std;

int main() {
    int A, B;
    cin >> A >> B;
    cout << A + B << endl; // A와 B를 더한 결과 출력
    cout << A - B << endl; // A와 B를 뺀 결과 출력
    cout << A * B << endl; // A와 B를 곱한 결과 출력
    cout << A / B << endl; // A를 B로 나눈 몫 출력
    cout << A % B << endl; // A를 B로 나눈 나머지 출력
    return 0;
}